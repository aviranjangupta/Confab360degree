         <h2>About the conference</h2>
         <p>Today's world faces challenging situations. Technological advancements, business transformations, or societal changes are the key drivers that are influencing business decisions across the globe. There is an urgent need to address these situations by formulating a robust framework and sustainable solutions. The major challenge is to learn to treat sustainable development as a framework for innovation and to use and extend established management principles to make this framework operational and effective. </p>
         <p>There have been various ways in which the business has responded to these challenges. Some businesses have been proactive and responded positively. This forces a positive societal change in the global business environment paving way for sustainable, innovative business practices. People describe sustainable development as an outcome-based long-term preposition by the society that binds social, economic, and environmental goals. </p>
		 <p>Business opportunities are being created by focusing on environmental and social concerns as they co-exist. Addressing them together will benefit not only the economy but the society as a whole. This core philosophy of constantly changing technology and sustainable innovative business practices that lead to the overall social well-being of the societies is the core focus of this proposed conference. Success is achieved when entrepreneurship and innovation also join hands together to address these challenges in more efficient ways.</p>
